# Operadores artmeticos

x = 5
y = 10

# Suma
print("Suma", x + y)

# Resta
print("Resta", x - y)

#  Multiplicacion
print("Multiplicacion", x * y)

# Division
print("Division", x / y)

# Modulo (resto)
print("Modulo", x % y)

# Potencia
print("Potencia", x ** y)

# Division entera
print("Division entera", x // y)

par = 8
print(par % 2 == 0)

